async function sendTrainerEmail(member, inbody, assessment, workout, nutrition) {
  console.log('[sendEmail] Sending trainer report email...');
  try {
    const auth = getGoogleAuth();
    const gmail = google.gmail({ version: 'v1', auth });

    const senderEmail = process.env.GMAIL_USER;

    const subject = `New Member — ${member.name} | ${member.primary_goal}`;
    const html = buildTrainerHtml(member, inbody, assessment, workout, nutrition);
    const raw = buildMimeMessage(process.env.TRAINER_EMAIL, senderEmail, subject, html);

    await gmail.users.messages.send({ userId: 'me', requestBody: { raw } });
    console.log(`[sendEmail] Trainer email sent to ${process.env.TRAINER_EMAIL}`);
  } catch (error) {
    console.error(`[sendEmail] Trainer email failed: ${error.message}`);
  }
}

async function sendClientEmail(member, assessment, workout, nutrition) {
  console.log('[sendEmail] Sending client welcome email...');
  try {
    const auth = getGoogleAuth();
    const gmail = google.gmail({ version: 'v1', auth });

    const gymName = process.env.GYM_NAME || 'Your Gym';
    const senderEmail = process.env.GMAIL_USER;

    const subject = `Welcome to ${gymName}, ${member.name} — Your Plan is Ready!`;
    const html = buildClientHtml(member, assessment, workout, nutrition, gymName);
    const raw = buildMimeMessage(member.email, senderEmail, subject, html);

    await gmail.users.messages.send({ userId: 'me', requestBody: { raw } });
    console.log(`[sendEmail] Client email sent to ${member.email}`);
  } catch (error) {
    console.error(`[sendEmail] Client email failed: ${error.message}`);
  }
}

